#To find the area of triangle using heron's formula

a= int(input("Enter your side 1 of triangle: "))
b= int(input("Enter your side 2 of triangle: "))
c= int(input("Enter your side 3 of triangle: "))
s= (a+b+c)/2
area= (s*(s-a)*(s-b)*(s-c))**(0.5)
print("area of triangle using heron's formula",area)
